					<p>Globally, Computer Engineering skills continue to be in high demand from businesses in many sectors and this field is experiencing explosive growth. This Computer Engineering degree is designed to meet the needs of these businesses, to pursue a rewarding career. The curriculum designed by SPPU is followed which combines theoretical knowledge with practical experiments to prepare for the world of work. This program is the right choice for students who aspire the IT sector and have that zeal to be a tool proficient.<br><br>
						The Department of Computer Engineering at Pimpri Chinchwad College of Engineering and Research (PCCOE&R) was established in the year 2015 with an initial intake of 60 students in undergraduate degree programme and enhanced its intake to 120 in the year 2020 and 180 in the year 2022. Department has been accredited by NBA for 3 years till June 2024.<br><br>
						PCCOE&R works on student-centric Four-Pillar philosophy which includes academic excellence, professional competence, research and innovation and social commitment. The Department also works on Outcome Based Philosophy (OBE) which is aligned with Four-Pillar philosophy, in which, we focus and organize the entire programme and all the instructional efforts around the clearly defined outcomes that we want our students to demonstrate when they leave the institute. We endeavor to deliver such contents and to exercise such pedagogies and assessment that our students achieve high order learning and mastery, instead of just accumulating credits.<br><br>
						Department runs a UG programme in Computer Engineering, which is a 4 years, 8 semesters full time programme. Department has adequate number of Faculty members and supporting staff, who are well qualified, experienced and dedicated. Department has spacious classrooms, tutorial room, a department lobby(e-Shishyakulam), a project & research lab and experimentation laboratories equipped with latest equipment which are used in field. The laboratories include Multimedia Lab, Web Development Lab, Programming Lab, DE & MP LAB, Computer Network Lab, Database Lab, Computer Center.<br><br>
						The department believes in student-centric, research oriented course delivery and aims to produce industry ready engineering workforce who are culturally vibrant, socially sensitive and enterprising with leadership qualities.<br><br>
						The department has exceptional placements record and offers skills to meet the challenges of a creative and productive profession according to industry needs. The department prides itself on its students for holding excellent placement records in various multinational companies, both in the public and private sectors.
						The Department has a Research and Innovation conducive environment wherein students are guided for publishing papers, filing IPRs and developing products..<br><br>
						The Department conducts a number of workshops, seminars, industrial training programs, alumni interactions, guest lectures, and expert lectures to enhance the knowledge and to make students aware of the latest technologies. Department has a professional student branch named ACM under which technical programs like eminent speaker programs from technical fields, different technical programs, etc. are organized. Department also focuses on the overall personality development of students through a student association club named C-Cube (Computer Cosmos Consortium) , a student chapter ACM (Association for computing Machinery), Google developer students club(GDSC) and Geeksforgeeks(GFG) student chapter.
